# Lists

mylist = ['Orange','Passion','Banana']
print(mylist)

mylist2 = [400,5000,600,4000,500,600,700]
print(mylist2)

print(mylist2[0])
print(mylist2[2:5])
print(mylist2[:6])
print(mylist2[2:])

# lists are changeable
mylist.append('Black berries')
mylist.remove('Orange')
mylist.insert(0,'Pawpaw')

print(mylist)